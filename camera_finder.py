try:
    resp = requests.get(url, headers=headers)
    if resp.status_code == 200:
        data = resp.json()
        countries = data['countries']

        for key, value in countries.items():
            print(f'Code : ({key}) - {value["country"]} / ({value["count"]})')

        country = input("Code(##) : ")
        res = requests.get(f"http://www.insecam.org/en/bycountry/{country}", headers=headers)
        if res.status_code == 200:
            last_page = re.findall(r'pagenavigator\("\?page=", (\d+)', res.text)
            if last_page:
                last_page = last_page[0]

                for page in range(int(last_page)):
                    res = requests.get(f"http://www.insecam.org/en/bycountry/{country}/?page={page}", headers=headers)
                    if res.status_code == 200:
                        find_ip = re.findall(r"http://\d+.\d+.\d+.\d+:\d+", res.text)

                        with open(f'{country}.txt', 'a') as f:
                            for ip in find_ip:
                                print("\033[1;31m", ip)
                                f.write(f'{ip}\n')
            else:
                print("No pages found.")
        else:
            print(f"Failed to get data for country code: {country}")
    else:
        print("Failed to get country list data.")
except Exception as e:
    print(f"An error occurred: {e}")
finally:
    print("\033[1;37m")
    print(f'Save File: {country}.txt')
    exit()
